function normalizeLang(input) {
  if (!input) return "fr";
  const lower = input.toLowerCase();
  if (lower.startsWith("en")) return "en";
  return "fr";
}

function getLangFromQuery() {
  const params = new URLSearchParams(window.location.search);
  return params.get("lang");
}

function updateLangInQuery(lang) {
  const url = new URL(window.location.href);
  url.searchParams.set("lang", lang);
  window.history.replaceState({}, "", url.toString());
}

function applyPageMeta(lang) {
  const page = document.documentElement.dataset.page || "home";
  const titleKey = page === "support" ? "meta_support_title" : "meta_home_title";
  const descriptionKey = page === "support" ? "meta_support_description" : "meta_home_description";
  const descriptionNode = document.querySelector('meta[name="description"]');
  const ogTitleNode = document.querySelector('meta[property="og:title"]');
  const ogDescriptionNode = document.querySelector('meta[property="og:description"]');

  document.title = translations[lang][titleKey] || document.title;

  if (descriptionNode && translations[lang][descriptionKey]) {
    descriptionNode.setAttribute("content", translations[lang][descriptionKey]);
  }

  if (ogTitleNode && translations[lang][titleKey]) {
    ogTitleNode.setAttribute("content", translations[lang][titleKey]);
  }

  if (ogDescriptionNode && translations[lang][descriptionKey]) {
    ogDescriptionNode.setAttribute("content", translations[lang][descriptionKey]);
  }
}

function applyLanguage(lang) {
  const selected = translations[lang] ? lang : "fr";
  document.documentElement.lang = selected;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const value = translations[selected][key];
    if (value) node.textContent = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((node) => {
    const key = node.getAttribute("data-i18n-alt");
    const value = translations[selected][key];
    if (value) node.setAttribute("alt", value);
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === selected);
  });

  applyPageMeta(selected);
  localStorage.setItem("flux-site-lang", selected);
  updateLangInQuery(selected);
}

function initLanguage() {
  const fromQuery = getLangFromQuery();
  const fromStorage = localStorage.getItem("flux-site-lang");
  const fromBrowser = navigator.language;
  const startLang = normalizeLang(fromQuery || fromStorage || fromBrowser);
  applyLanguage(startLang);
}

function setupReveal() {
  const nodes = document.querySelectorAll(".reveal, [data-reveal]");
  if (!nodes.length) return;

  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion || !("IntersectionObserver" in window)) {
    nodes.forEach((node) => node.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -8% 0px"
    }
  );

  nodes.forEach((node, index) => {
    node.style.setProperty("--reveal-delay", `${Math.min(index, 4) * 70}ms`);
    observer.observe(node);
  });
}

function setupHeroParallax() {
  const heroVisual = document.querySelector(".hero-visual");
  if (!heroVisual) return;

  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion) return;

  let ticking = false;

  const update = () => {
    const offset = Math.max(-72, window.scrollY * -0.08);
    heroVisual.style.setProperty("--hero-parallax", `${offset}px`);
    ticking = false;
  };

  update();

  window.addEventListener(
    "scroll",
    () => {
      if (ticking) return;
      ticking = true;
      window.requestAnimationFrame(update);
    },
    { passive: true }
  );
}

document.documentElement.classList.add("js-ready");

document.addEventListener("DOMContentLoaded", () => {
  initLanguage();
  setupReveal();
  setupHeroParallax();

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
});
